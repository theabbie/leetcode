class Solution:
    def check(self, m, curr, a, i):
        if curr <= m:
            self.mcap = max(self.mcap, curr)
            if i < len(a):
                self.check(m, curr + a[i], a, i + 1)
                self.check(m, curr, a, i + 1)

    def back_pack(self, m, a):
        self.mcap = 0
        self.check(m, 0, a, 0)
        return self.mcap

